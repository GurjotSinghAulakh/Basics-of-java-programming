package kontrollstrukturer2;

public class Oppgave2 {
    public static void main(String[] args) {

        // Adding the numbers from 0 to 9.
        int sum = 0;
        for (int i = 0; i < 10; i++) {
            sum += i;
        }

        // It prints the sum of the numbers from 0 to 9.
        System.out.println("Summen blir: " + sum);
    }
}
