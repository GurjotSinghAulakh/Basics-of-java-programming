package aritmetikk;

/**
 * It compares two integers and prints out the result of the comparison
 */ 

public class Oppgave9 {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 4;

        System.out.println(num1 == num2);
        System.out.println(num1 != num2);
        System.out.println(num1 <= num2);
        System.out.println(num1 >= num2);
        System.out.println(num1 < num2);
        System.out.println(num1 > num2);

    }
}
