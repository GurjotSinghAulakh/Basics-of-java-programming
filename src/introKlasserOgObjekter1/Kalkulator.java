package introKlasserOgObjekter1;

public class Kalkulator {
    
    /**
     * It takes two numbers, adds them together, and returns the result
     * 
     * @param tall1 The first number to add
     * @param tall2 The base number
     * @return The sum of a and b.
     */

    public static double addere(double tall1, double tall2) {
        return tall1 + tall2;
    }

    /**
     * "subtrahere" is a function that takes two numbers and returns their difference
     * 
     * @param tall1 The first number to be subtracted from.
     * @param tall2 The base number.
     * @return The difference between the two numbers.
     */

    public static double subtrahere(double tall1, double tall2) {
        return tall1 - tall2;
    }

    /**
     * The function multiplisere takes two doubles as input and returns the product of the two doubles
     * 
     * @param tall1 The first number to be multiplied.
     * @param tall2 The second number to multiply.
     * @return The method is returning the value of the two numbers multiplied together.
     */

    public static double multiplisere(double tall1, double tall2) {
        return tall1 * tall2;
    }   

    /**
     * This function takes two numbers, divides them, and returns the result.
     * 
     * @param tall1 The first number
     * @param tall2 The number you want to divide by
     * @return The result of the calculation.
     */

    public static double dividere(double tall1, double tall2) {
        return tall1 / tall2;
    }
}
